имеет(Жена, Вещь) :-
    женат(Муж, Жена),
    имеет(Муж, Вещь).

имеет_минимальный_баланс(Лицо, Баланс) :-
    имеет(Лицо, руб(Сколько)),
    Сколько >= Баланс.

цена(машина("Волга"), руб(32000)).
