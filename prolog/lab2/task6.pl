цена(видео, руб(12000)).
цена(приемник, руб(1300)).
цена(часы, руб(500)).
 
может_купить(Лицо, Товар) :-
    цена(Товар, руб(Стоимость)),
    имеет(Лицо, руб(Баланс)),
    Баланс >= Стоимость.
 
может_купить_новое(Лицо, Товар) :- 
    \+ имеет(Лицо, Товар),
    может_купить(Лицо, Товар).

